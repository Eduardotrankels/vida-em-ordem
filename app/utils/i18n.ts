export function resolveAppLanguage(value?: string | null): AppLanguage {
  return LANGUAGE_OPTIONS.some((option) => option.value === value)
    ? (value as AppLanguage)
    : DEFAULT_APP_LANGUAGE;
}

export function detectDeviceLanguage(): AppLanguage {
  try {
    const locale =
      Intl.DateTimeFormat().resolvedOptions().locale?.toLowerCase() || "";
    const baseLanguage = locale.split(/[-_]/)[0];

    if (
      baseLanguage === "pt" ||
      baseLanguage === "en" ||
      baseLanguage === "es" ||
      baseLanguage === "fr" ||
      baseLanguage === "it"
    ) {
      return baseLanguage;
    }

    return "en";
  } catch (error) {
    console.log("Erro ao detectar idioma do dispositivo:", error);
    return DEFAULT_APP_LANGUAGE;
  }
}

export function getLanguageOption(language: AppLanguage) {
  return (
    LANGUAGE_OPTIONS.find((option) => option.value === language) ||
    LANGUAGE_OPTIONS[0]
  );
}

export function translate(
  language: AppLanguage,
  key: string,
  params?: TranslationParams
) {
  const fallback = translations.pt[key] || key;
  const template = translations[language][key] || fallback;

  if (!params) {
    return template;
  }

  return Object.entries(params).reduce((message, [paramKey, value]) => {
    return message.replaceAll(`{{${paramKey}}}`, String(value));
  }, template);
}
